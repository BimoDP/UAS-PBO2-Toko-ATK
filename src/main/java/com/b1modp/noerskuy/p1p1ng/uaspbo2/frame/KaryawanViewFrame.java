    private void cariButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariButtonActionPerformed
        try {
            String kataKunci = cariTextField.getText();
            try (Connection koneksi = Database.getConnection()) {
                String selectSQL = "SELECT * FROM karyawan WHERE nama_karyawan like '%"+kataKunci+"%' OR posisi like '%"+kataKunci+"%' OR telp like '%"+kataKunci+"%' OR alamat like '%"+kataKunci+"%' OR email like '%"+kataKunci+"%' " ;
                java.sql.Statement statement = koneksi.createStatement();
                ResultSet resultSet = statement.executeQuery(selectSQL);
                
                Object[] row = new Object[6];
                DefaultTableModel model = (DefaultTableModel) viewTable.getModel();
                model.setRowCount(0);
                
                while(resultSet.next()){
                    row[0] = resultSet.getInt("id_karyawan");
                    row[1] = resultSet.getString("nama_karyawan");
                    row[2] = resultSet.getString("posisi");
                    row[3] = resultSet.getString("telp");
                    row[4] = resultSet.getString("alamat");
                    row[5] = resultSet.getString("email");
                    model.addRow(row);
                }
            }
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        };
    }//GEN-LAST:event_cariButtonActionPerformed

