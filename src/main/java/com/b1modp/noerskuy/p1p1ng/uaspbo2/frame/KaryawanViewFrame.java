    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
setLocationRelativeTo(null);
        try {
            Connection koneksi = Database.getConnection();
            String selectSQL = "SELECT * FROM karyawan";
            java.sql.Statement statement = koneksi.createStatement();
            ResultSet resultSet = statement.executeQuery(selectSQL);

            Object[] row = new Object[6];
            DefaultTableModel model = (DefaultTableModel) viewTable.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                row[0] = resultSet.getInt("id_karyawan");
                row[1] = resultSet.getString("nama_karyawan");
                row[2] = resultSet.getString("posisi");
                row[3] = resultSet.getString("telp");
                row[4] = resultSet.getString("alamat");
                row[5] = resultSet.getString("email");
                model.addRow(row);
            }
            koneksi.close();
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_formWindowActivated

